const generatePlateData = () => {
  const data: number[][] = [];
  const rows = 16;
  const cols = 24;

  for (let i = 0; i < rows; i++) {
    const row: number[] = [];
    for (let j = 0; j < cols; j++) {
      let value = 5000 + Math.random() * 25000;

      if ((i + j) % 7 === 0) {
        value += 10000;
      }

      if ((i === 11 || i === 12) && j === 12) {
        value = 45000 + Math.random() * 5000;
      }

      row.push(Math.round(value));
    }
    data.push(row);
  }

  return data;
};

